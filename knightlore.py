all_symbols = " ◢◣◤◥▄▀█"

def build_values(src_line):
    line0 = knightlore_lines[src_line]
    line1 = knightlore_lines[src_line + 1]
    return [line0[n] + line1[n]*2 for n in range(20)]

def build_line(src_line):
    vals = [0, 6, 5, 7]
    return (vals[n] for n in build_values(src_line))

def build_comment(src_line):
    return (all_symbols[n] for n in build_line(src_line))


